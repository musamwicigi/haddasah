            <div class="entry-meta-data"> <span class="author"> <i class="fa fa-user"></i>&nbsp; By: <a href="single_post.html#">Admin</a></span> <span class="cat"> <i class="fa fa-folder"></i>&nbsp; <a href="single_post.html#">News, </a> <a href="single_post.html#">Promotions</a> </span> <span class="comment-count"> <i class="fa fa-comment"></i>&nbsp; 3 </span> 
