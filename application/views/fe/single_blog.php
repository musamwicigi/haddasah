            <div class="content-text clearfix">
              <p>Greetings, beaders! I have been putting off a return to the blog for quite some time, mainly because I haven’t been certain for many weeks whether or not I would continue to make things or write about them on a regular basis. The good news is that if I don’t get started now, I will have too many projects to update on, and some things will have to be left behind. Since we can’t have that, it’s time to start catching up.</p>

<p>For the last several months I’ve been completely laid out with the worst case of beader’s block I’ve ever experienced. It would be appropriate to call it complete creative burn out. The last couple of years were very busy around here, both creatively and life-wise. In the past I’ve had a lot of success in challenging myself to write and create every day, but this time around it spelled certain doom for my beads and I. After taking a break in December to work on personal projects, I simply couldn’t bring myself to make anything new. I felt overwhelmed by obligations and underwhelmed by inspiration. I had a terrible case of the don’t-wannas. I couldn't even find the energy to check my emails everyday. It was all too easy to fall into a new routine where I could relax, nap, read, or binge-watch whenever I wanted. I have to admit that I was enjoying the down time a lot. Part of me was very worried that I might not ever want to do beadwork again.</p>

<p>At first I thought I just needed a change of pace, so I put away the spiral rope necklace that I was working on and tried to reboot. Unfortunately no new projects really came to mind. I did a bit of an assessment and decided that I needed to cull some of my pursuits so that I could focus on what I really wanted to do. The first step was to deactivate my jewelry shop. This was hard to do at first, since I’ve put so much effort into it over the years, but I knew that the obligation of maintaining it was draining my energy. When I did my taxes and saw my revenue for the year, any regret I felt vanished. It was time.</p>



<p>Although I did feel a lot lighter after dropping that ball and chain, I still didn’t have a lot of desire to create. My next step was to unpack all of the jewelry from my shop and set it up in my own wardrobe. I was hoping for a boost of inspiration there, but it actually had the opposite effect. If I wasn’t creating for the shop anymore, I would end up wearing anything I made, and now I had more jewelry than I could ever need. It has been absolutely fabulous to be able to wear all of my favorite pieces, and after a few weeks I realized that there were a few things I wouldn’t mind having more of – especially if I could design them just for my own personal style. I pulled out some beads to make a new cuff bracelet in my favorite color and pattern – green with black and white stripes – but I got bored after only an inch of work. I was still too attached to my new routine and the design just wasn’t juicy enough to keep me at my workspace.</p>



